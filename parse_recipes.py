# MAIN
# =============================================================================

def main():
    parser = argparse.ArgumentParser(
        description="Parse one or more recipe transcription PDFs into a structured JSON database.",
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""\
Examples:
  # Parse multiple PDFs into a single clean database
  python parse_recipes.py firstp.11.pdf next.83.pdf next.183.pdf

  # Parse all PDFs in the directory
  python parse_recipes.py *.pdf --output recipes.json

  # Save each recipe as an individual file in a folder
  python parse_recipes.py *.pdf --output-dir dataset/

  # Parse from raw Markdown instead of PDF
  python parse_recipes.py transcription.md --markdown

  # Resume a previously interrupted run
  python parse_recipes.py *.pdf --resume
"""
    )
    parser.add_argument("input_files", nargs="+",
                        help="Path(s) to transcription PDF(s) (or Markdown with --markdown)")
    parser.add_argument("--output", "-o", default=None,
                        help="Output JSON file path (default: recipes.json)")
    parser.add_argument("--output-dir", "-d", default=None,
                        help="Output directory for individual recipe files")
    parser.add_argument("--markdown", action="store_true",
                        help="Input is raw Markdown instead of PDF")
    parser.add_argument("--models", nargs="+", default=DEFAULT_MODEL_CHAIN,
                        help="Model fallback chain (default: Nemotron → Gemini 3 → qwen 397b → Gemini 2.5 → qwen 9b)")
    parser.add_argument("--api-key", default=os.getenv("GEMINI_API_KEY", ""),
                        help="Gemini API key (default: env GEMINI_API_KEY)")
    parser.add_argument("--openrouter-key", default=os.getenv("OPENROUTER_API_KEY", ""),
                        help="OpenRouter API key (default: env OPENROUTER_API_KEY)")
    parser.add_argument("--resume", action="store_true",
                        help="Resume parsing from a saved progress file")

    args = parser.parse_args()

    # Validate inputs
    for fp in args.input_files:
        if not os.path.exists(fp):
            print(f"Error: File not found: {fp}")
            sys.exit(1)

    print(f"\n{'='*60}")
    print(f"Recipe Dataset Parser")
    print(f"{'='*60}")
    print(f"Input files: {len(args.input_files)}")
    for fp in args.input_files:
        print(f"  • {fp}")
    print(f"Models: {' → '.join(args.models)}")
    print(f"OpenRouter key: {'set' if args.openrouter_key else 'NOT SET (OpenRouter models will be skipped)'}")
    print(f"Gemini API key: {'set' if args.api_key else 'NOT SET (Gemini models will be skipped)'}")

    # Extract & merge text from all files
    print(f"\n--- Extracting text ---")
    text = merge_texts(args.input_files, is_markdown=args.markdown)

    print(f"\nTotal merged text: {len(text)} chars")
    print(f"{'='*60}\n")

    # Chunk
    chunks = chunk_text_by_recipes(text)
    print(f"Document split into {len(chunks)} potential recipe chunks.\n")

    # Progress saving
    progress_file = Path(".parsing_progress.json")
    recipes = []
    start_index = 0

    if args.resume and progress_file.exists():
        try:
            with open(progress_file, "r", encoding="utf-8") as f:
                saved_state = json.load(f)
                recipes = saved_state.get("recipes", [])
                start_index = saved_state.get("last_processed_index", -1) + 1
                if start_index < len(chunks):
                    print(f"📂 Resuming from chunk {start_index+1}/{len(chunks)} "
                          f"({len(recipes)} recipes done)\n")
                else:
                    print("All chunks were already processed.")
        except Exception as e:
            print(f"Warning: Could not resume from progress file: {e}")

    # Process chunks via LLM
    print("Starting LLM Extraction...\n")

    for i in range(start_index, len(chunks)):
        chunk = chunks[i]
        title_hint = chunk.split("\n")[0][:60]

        print(f"  [{i+1}/{len(chunks)}] {title_hint}")

        recipe_data = extract_recipe_with_llm(chunk, args.models, api_key=args.api_key, openrouter_key=args.openrouter_key)

        if recipe_data:
            recipe_data["numero"] = len(recipes) + 1
            recipes.append(recipe_data)
            print(f"      ✅ {recipe_data['titre']} "
                  f"({len(recipe_data['ingredients'])} ingr., "
                  f"{len(recipe_data['preparation'])} étapes)")
        else:
            print(f"      ⏭️  Skipped (not a valid recipe)")

        # Save progress after every chunk
        with open(progress_file, "w", encoding="utf-8") as f:
            json.dump({
                "last_processed_index": i,
                "recipes": recipes
            }, f, ensure_ascii=False)

    print(f"\n--- Extraction complete ---")
    print(f"Raw recipes extracted: {len(recipes)}")

    if not recipes:
        print("No recipes found. Check the input files or the LLM output.")
        sys.exit(1)

    # Deduplicate
    print(f"\n--- Deduplicating ---")
    before = len(recipes)
    recipes = deduplicate_recipes(recipes)
    after = len(recipes)
    if before != after:
        print(f"  Removed {before - after} duplicate(s).")
    else:
        print(f"  No duplicates found.")
    print(f"  Final: {after} unique recipe(s).\n")

    # Preview
    print("Preview:")
    for r in recipes[:5]:
        ing_count = len(r.get('ingredients', []))
        prep_count = len(r.get('preparation', []))
        print(f"  {r['numero']}. {r['titre']} ({ing_count} ingrédients, {prep_count} étapes)")
    if len(recipes) > 5:
        print(f"  ... et {len(recipes) - 5} de plus\n")
    else:
        print()

    # Save final output
    if args.output_dir:
        save_as_individual_files(recipes, args.output_dir)
    else:
        output_path = args.output or "recipes.json"
        save_as_json(recipes, output_path)

    # Clean up progress file on success
    try:
        if progress_file.exists():
            progress_file.unlink()
    except OSError:
        pass

    print(f"\n{'='*60}")
    print("DONE")
    print(f"{'='*60}\n")


if __name__ == "__main__":
    main()
